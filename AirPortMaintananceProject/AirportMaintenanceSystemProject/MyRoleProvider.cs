        public override string[] GetRolesForUser(string username)
        {
            using (var context = new AirportMaintenanceSystemEntities())
            {
                var result = (from UserLoginRegister in context.UserLoginRegisters
                              join Role in context.Roles on UserLoginRegister.RoleId equals Role.RoleId
                              where UserLoginRegister.UserName == username
                              select Role.RoleTitle).ToArray();
                return result;
            }
